# 1. Convertir int a string
# El número 100 se convierte en el texto "100"
print(str(100))
# 2. Convertir float a string
# El número 3.14 se convierte en el texto "3.14"
print(str(3.14))

# 3. Convertir booleano a string
# Primero evalúa si 2 es mayor que 1 (Resultado: True)
# Luego convierte ese True en texto "True"
print(str(2 > 1))