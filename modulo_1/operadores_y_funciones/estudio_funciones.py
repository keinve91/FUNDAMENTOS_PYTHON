# Una función es un bloque de código reutilizable que realiza una tarea específica.

# 1. Máximo y Mínimo
# max(): Devuelve el valor más alto
print("Máximo de (4, 7, 23, 11):", max(4, 7, 23, 11))

# min(): Devuelve el valor más bajo
print("Mínimo de (4, 7, 23, 11):", min(4, 7, 23, 11))

# 2. Matemáticas básicas
# abs(): Valor absoluto (quita el signo negativo)
print("Valor absoluto de -9.4:", abs(-9.4))

# sum(): Suma todos los elementos de una lista
print("Suma de [1, 2, 3]:", sum([1, 2, 3]))

# round(): Redondea un número. El segundo argumento es la cantidad de decimales.
print("Redondear 4.91278 a 2 decimales:", round(4.91278, 2))

# pow(): Potencia (equivalente a **). Base, Exponente.
print("Potencia pow(2, 3):", pow(2, 3))

# 3. Utilidades
# len(): Devuelve la longitud (cantidad de ítems) de una lista o secuencia
lista = [1, 3, 5, 7, 9]
print("Longitud de la lista:", len(lista))